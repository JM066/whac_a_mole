export function getWords() {
  return new Promise<typeof words>((resolve, reject) => {
    setTimeout(() => {
      resolve(words)
    }, getRandomInt(50, 300))
  })
}
const getRandomInt = (min: number, max: number) => {
  min = Math.ceil(min)
  max = Math.floor(max)
  return Math.floor(Math.random() * (max - min) + min)
}
